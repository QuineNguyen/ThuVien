package model;

public class Tacgia447 {
    private int id;
    private String ten;
    private String mota;
    private String diachi;
    
    public Tacgia447(int id, String ten, String mota, String diachi) {
        this.id = id;
        this.ten = ten;
        this.mota = mota;
        this.diachi = diachi;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
}
